def adjust_correlation_matrix(corr_matrix, multiplier):
    """
    Adjusts off-diagonal correlations while keeping diagonal values equal to 1.
    """

    adjusted_corr = corr_matrix.copy()

    for i in range(adjusted_corr.shape[0]):
        for j in range(adjusted_corr.shape[1]):
            if i != j:
                adjusted_corr.iloc[i, j] = adjusted_corr.iloc[i, j] * multiplier

    adjusted_corr = adjusted_corr.clip(-0.99, 0.99)

    for i in range(adjusted_corr.shape[0]):
        adjusted_corr.iloc[i, i] = 1.0

    return adjusted_corr


