def calculate_risk_metrics(portfolio_paths, initial_portfolio_value):
    """
    Calculates expected value, VaR, CVaR, probability of loss,
    and maximum drawdown.
    """

    final_values = portfolio_paths[-1]

    profit_loss = final_values - initial_portfolio_value

    expected_final_value = np.mean(final_values)

    expected_profit_loss = np.mean(profit_loss)

    var_95 = np.percentile(profit_loss, 5)

    var_99 = np.percentile(profit_loss, 1)

    cvar_95 = profit_loss[profit_loss <= var_95].mean()

    cvar_99 = profit_loss[profit_loss <= var_99].mean()

    probability_loss = np.mean(final_values < initial_portfolio_value)

    running_max = np.maximum.accumulate(portfolio_paths, axis=0)

    drawdown = (portfolio_paths - running_max) / running_max

    max_drawdown = drawdown.min(axis=0).mean()

    return {
        "Expected Final Value": expected_final_value,
        "Expected Profit/Loss": expected_profit_loss,
        "VaR 95%": var_95,
        "VaR 99%": var_99,
        "CVaR 95%": cvar_95,
        "CVaR 99%": cvar_99,
        "Probability of Loss": probability_loss,
        "Average Maximum Drawdown": max_drawdown
    }

