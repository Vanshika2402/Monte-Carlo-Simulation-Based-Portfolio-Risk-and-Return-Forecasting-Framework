"""MONTE CARLO SCENARIO FUNCTION"""

def run_monte_carlo_scenario(
    weights,
    scenario_mean,
    scenario_cov,
    num_simulations=10000,
    forecast_days=252,
    initial_portfolio_value=100000,
    random_seed=42
):
    """
    Runs Monte Carlo simulation under a specific market scenario.
    """

    rng = np.random.default_rng(random_seed)

    simulated_asset_returns = rng.multivariate_normal(
        mean=scenario_mean,
        cov=scenario_cov,
        size=(forecast_days, num_simulations)
    )

    portfolio_daily_returns = simulated_asset_returns @ weights

    portfolio_paths = initial_portfolio_value * np.cumprod(
        1 + portfolio_daily_returns,
        axis=0
    )

    return portfolio_paths

