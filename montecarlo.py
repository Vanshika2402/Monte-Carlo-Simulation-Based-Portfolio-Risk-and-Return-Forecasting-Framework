"""POTFOLIO CONSTRUCTION"""

risk_free_rate = 0.04
def portfolio_return(weights, mean_returns):
    """
    Calculates expected portfolio return.
    Formula: Rp = w' * mu
    """
    return np.dot(weights, mean_returns)


def portfolio_volatility(weights, covariance_matrix):
    """
    Calculates portfolio volatility.
    Formula: sigma_p = sqrt(w' * Sigma * w)
    """
    return np.sqrt(np.dot(weights.T, np.dot(covariance_matrix, weights)))


def sharpe_ratio(weights, mean_returns, covariance_matrix, risk_free_rate):
    """
    Calculates Sharpe Ratio.
    Formula: Sharpe = (Rp - Rf) / sigma_p
    """
    ret = portfolio_return(weights, mean_returns)
    vol = portfolio_volatility(weights, covariance_matrix)

    return (ret - risk_free_rate) / vol

